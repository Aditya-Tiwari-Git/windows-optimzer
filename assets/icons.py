def get_icon(name):
    """
    Get a QIcon from the icon collection by name.
    
    Args:
        name (str): Icon name
        
    Returns:
        QIcon: Icon object
    """
    if name not in ICONS:
        return QIcon()
    
    # Get SVG data
    svg_data = ICONS[name].strip().encode('utf-8')
    
    # Create QIcon from SVG data
    byte_array = QByteArray(svg_data)
    buffer = QBuffer(byte_array)
    buffer.open(QIODevice.ReadOnly)
    
    icon = QIcon()
    icon.addFile("", size=None, data=buffer)
    
    return icon

def get_svg_data(name):
    """
    Get raw SVG data for an icon by name.
    
    Args:
        name (str): Icon name
        
    Returns:
        str: SVG data or empty string if not found
    """
    return ICONS.get(name, "").strip()
